    // ðŸ§± Inisialisasi model tabel
    void setupTableModels() {
        modelPemasukan = new DefaultTableModel(new String[]{"Nama", "NIK", "No HP", "Tanggal", "Jumlah"}, 0);
        modelPengeluaran = new DefaultTableModel(new String[]{"Nama", "NIK", "No HP", "Tanggal", "Jumlah"}, 0);

        tblpemasukan.setModel(modelPemasukan);
        tblpengeluaran.setModel(modelPengeluaran);
    }

    // ðŸ“¥ Load data dari database ke tabel
    void loadData() {
        try {
            modelPemasukan.setRowCount(0);
            modelPengeluaran.setRowCount(0);
            Statement st = cn.createStatement();
            
            // Load data pemasukan dari data_warga
            ResultSet rsPemasukan = st.executeQuery("SELECT Nama_Warga, NIK, No_HP, Tanggal, Jumlah FROM data_warga WHERE Kategori = 'Pemasukan' OR Kategori = 'pemasukan'");
            while (rsPemasukan.next()) {
                // Konversi jumlah ke angka
                String jumlahStr = rsPemasukan.getString("Jumlah");
                String cleanJumlah = jumlahStr.replace("RP. ", "").replace("RP ", "").replace("RP.", "").replace(".", "").replace(",", "");
                int jumlahInt = 0;
                try {
                    jumlahInt = Integer.parseInt(cleanJumlah);
                } catch (NumberFormatException e) {
                    jumlahInt = 0;
                }
                
                Object[] row = {
                    rsPemasukan.getString("Nama_Warga"),
                    rsPemasukan.getString("NIK"),
                    rsPemasukan.getString("No_HP"),
                    rsPemasukan.getString("Tanggal"),
                    jumlahInt
                };
                modelPemasukan.addRow(row);
            }
            rsPemasukan.close();
            
            // Load data pengeluaran dari data_warga
            ResultSet rsPengeluaran = st.executeQuery("SELECT Nama_Warga, NIK, No_HP, Tanggal, Jumlah FROM data_warga WHERE Kategori = 'Pengeluaran' OR Kategori = 'pengeluaran'");
            while (rsPengeluaran.next()) {
                // Konversi jumlah ke angka
                String jumlahStr = rsPengeluaran.getString("Jumlah");
                String cleanJumlah = jumlahStr.replace("RP. ", "").replace("RP ", "").replace("RP.", "").replace(".", "").replace(",", "");
                int jumlahInt = 0;
                try {
                    jumlahInt = Integer.parseInt(cleanJumlah);
                } catch (NumberFormatException e) {
                    jumlahInt = 0;
                }
                
                Object[] row = {
                    rsPengeluaran.getString("Nama_Warga"),
                    rsPengeluaran.getString("NIK"),
                    rsPengeluaran.getString("No_HP"),
                    rsPengeluaran.getString("Tanggal"),
                    jumlahInt
                };
                modelPengeluaran.addRow(row);
            }
            rsPengeluaran.close();
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal load data: " + e.getMessage());
        }
    }

  

